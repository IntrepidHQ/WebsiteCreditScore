export function getNicheCompetitors(
  niche: SiteNiche,
  excludeUrl: string,
  limit = 3,
): NicheCompetitor[] {
  const pool = nicheCompetitorPool[niche] ?? nicheCompetitorPool["generic"];

  let excludeHostname = "";
  try {
    excludeHostname = new URL(excludeUrl).hostname.toLowerCase().replace(/^www\./, "");
  } catch {
    // ignore invalid URLs
  }

  return pool
    .filter((competitor) => {
      try {
        const competitorHostname = new URL(competitor.url).hostname.toLowerCase().replace(/^www\./, "");
        return competitorHostname !== excludeHostname;
      } catch {
        return true;
      }
    })
    .sort((a, b) => b.curatedWeight - a.curatedWeight)
    .slice(0, limit);
}

export function nicheCompetitorsToReferences(
  competitors: NicheCompetitor[],
  vertical: BenchmarkVertical,
): BenchmarkReference[] {
  return competitors.map((competitor) => ({
    id: competitor.id,
    siteId: competitor.id,
    vertical,
    tier: competitor.tier,
    name: competitor.name,
    url: competitor.url,
    sourceLabel: "Industry competitor",
    note: competitor.note,
    previewImage: createWebsiteScreenshotUrl(competitor.url, "desktop"),
    mobilePreviewImage: createWebsiteScreenshotUrl(competitor.url, "mobile"),
    targetScore: getBenchmarkTargetScore(competitor.tier),
    strengths: competitor.strengths,
    whatWorks: competitor.whatWorks,
    bestFor: competitor.bestFor,
    reusablePatterns: competitor.reusablePatterns,
  }));
}
